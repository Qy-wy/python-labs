def arifmetics(a, b):
    print("Sum = ", a + b)
    print("Diff = ", a - b)
    print("Product = ", a * b)
    print("Division = ", a / b)
    print("Integer division = ", a // b)
    print("Modulo = ", a % b)
    print("Power = ", a ** b)

A = int(input("Enter a: "))
B = int(input("Enter b: "))
arifmetics(A, B)